namespace Model
{
    public class ModelVector3
    {
        public float X;
        public float Y;
        public float Z;

        public ModelVector3(float x, float y, float z)
        {
            X = x;
            Y = y;
            Z = z;
        }
        public ModelVector3()
        {
            X = 0;
            Y = 0;
            Z = 0;
        }
    }
}
